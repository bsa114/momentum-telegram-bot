"""
Заполняет xlsx-анкету на основе расшифровки визита, используя Claude (через
OpenRouter — тот же провайдер, что уже подключён в Dify) и системный промпт
"Инспектора" (тот же, что используется в Claude.ai проекте).

Модель не трогает файл напрямую — она возвращает строгий JSON вида
{"filled": [{"row": N, "score": X, "comment": "..."}], "questions": [...]},
а запись в реальные ячейки xlsx делает код (openpyxl), не модель.
Это надёжнее, чем полагаться на то, что модель сама аккуратно работает с файлом.
"""

import json
import os
import urllib.error
import urllib.request

from xlsx_structure import build_compact_template_text, extract_template_structure

OPENROUTER_API_URL = "https://openrouter.ai/api/v1/chat/completions"
OPENROUTER_MODEL = "anthropic/claude-sonnet-5"

PROMPT_DIR = os.path.join(os.path.dirname(__file__), "prompts")
with open(os.path.join(PROMPT_DIR, "inspector_agent_v2.md"), "r", encoding="utf-8") as f:
    INSPECTOR_SYSTEM_PROMPT = f.read()

OUTPUT_FORMAT_INSTRUCTIONS = """

## ВАЖНОЕ ДОПОЛНЕНИЕ К ФОРМАТУ ОТВЕТА (для автоматической обработки)

Ты работаешь не в интерактивном чате, а как часть автоматического Telegram-бота.
Поэтому вместо обычного диалога с уточняющими вопросами по одному, ты должен
вернуть ОДИН СТРОГИЙ JSON-объект и ничего больше — ни преамбулы, ни пояснений,
ни markdown-разметки вокруг JSON.

Формат ответа:
{
  "header": {"restaurant": "...", "date": "...", "waiter_name": "...", "waiter_description": "...", "visit_type": "...", "time_in": "...", "time_out": "..."},
  "filled": [
    {"row": 34, "score": 2, "comment": "текст комментария инспектора"}
  ],
  "questions": [
    {
      "section": "название раздела",
      "topic": "что нужно уточнить",
      "context": "что видно из расшифровки по этому поводу",
      "options": [
        {"label": "1", "row": 40, "score": 3, "comment": "готовый комментарий для этого варианта"},
        {"label": "2", "row": 41, "score": 0, "comment": "готовый комментарий для этого варианта"}
      ]
    }
  ]
}

Правила:
- "filled" — все пункты анкеты, которые ты можешь закрыть уверенно по правилам фазы Б.
  Указывай ТОЧНУЮ строку (row) из структуры анкеты, которая соответствует выбранной
  позиции шкалы — не первую строку блока, а именно ту, что выбрана.
- "questions" — все пункты, которые в обычной работе ты вынес бы в фазу В (уточнения).
  Для каждого варианта ответа заранее укажи row и score, которые нужно проставить,
  если пользователь выберет этот вариант.
- Не включай в ответ никакого текста вне JSON.
- Строго следуй техническим правилам записи (балл в нужную строку блока, не в первую),
  правилам жанра комментариев (прошедшее время, без имён, без оценочных слов,
  фактологично, с цитатами) — они остаются в силе, просто результат подаётся как JSON,
  а не как текст в чат.

## КРИТИЧЕСКИ ВАЖНОЕ ПРАВИЛО — ОДНА ЗАПИСЬ НА ОДИН ВОПРОС

Для обычного (не суммируемого) вопроса анкеты в "filled" должна попасть РОВНО ОДНА
запись — та строка, что соответствует фактически выбранному варианту. Никогда не
добавляй в "filled" одновременно две строки одного и того же вопроса, даже если
одна из них кажется "тоже подходящей".

Пример РЕАЛЬНОЙ ошибки, которую нужно избегать: у вопроса "Работа с меню" было два
варианта — "При подаче меню рассказали про акции" (row 42, балл 1) и "Подача меню
была молчаливой" (row 43, балл 0). Расшифровка показывала, что подача была
молчаливой. Правильно — заполнить ТОЛЬКО row 43 с баллом 0 и комментарием.
Неправильно (частая ошибка) — заполнить И row 42 с баллом 1 без комментария,
И row 43 с баллом 0 и комментарием. Вторая (лишняя) запись создаёт противоречие:
в файле окажется засчитан балл за то, чего не было.

Перед тем как добавить запись в "filled", проверь: не добавил ли ты уже другую
строку из того же блока вариантов этого же вопроса. Единственное исключение —
блоки с суммируемыми независимыми критериями (см. системный промпт выше,
раздел про слагаемые) — там правда заполняется несколько строк одновременно,
но КАЖДАЯ такая строка должна иметь свой собственный непустой комментарий,
без исключений.

## УРОКИ ИЗ РЕАЛЬНОГО СРАВНЕНИЯ С ОПЫТНЫМ ИНСПЕКТОРОМ

Ниже — конкретные системные ошибки, которые ты допускал в предыдущих визитах,
когда твой черновик сверяли построчно с правками живого инспектора. Изучи их
внимательно — это не гипотетические риски, а реально повторявшиеся паттерны.

**1. Не засчитывай критерий как выполненный по частичному совпадению.** Если
критерий требует НЕСКОЛЬКИХ условий сразу (например: "уточнили дату, время,
количество персон, имя И телефон, повторили и попрощались"), а в расшифровке
выполнены не все условия — это НЕ максимальный балл, даже если большая часть
пунктов выполнена. Пример реальной ошибки: сотрудник уточнил дату, время,
количество и имя, повторил и попрощался — но не запросил телефон. Это было
ошибочно засчитано как "всё без нареканий", хотя правильный балл — минимальный,
с комментарием о том, что именно упущено. Перечитай формулировку критерия
дословно и проверь В РАСШИФРОВКЕ каждое перечисленное в нём условие по отдельности.

**2. Один хороший эпизод не перекрывает общую картину визита.** Если ты нашёл
один позитивный момент (официант один раз предложил доливку, один раз что-то
уточнил) — это не значит, что весь пункт анкеты про "постоянную инициативу
в течение визита" закрыт максимальным баллом. Проверь, повторялось ли это
поведение системно, или это был единичный эпизод на фоне пассивности в
остальное время. Пример реальной ошибки: единственный эпизод "предложил
принести ещё кофе взамен холодного" был неверно расценен как признак общей
проактивности с напитками, хотя по остальной части визита официант напитки
не предлагал вообще.

**3. Ищи прямую речь внимательнее — не пропускай реплики, меняющие оценку.**
В одном случае модель написала "официант не запрашивал обратную связь",
хотя в расшифровке буквально была реплика "Всё вкусно? Девушка, вам?" —
прямо противоположный факт. Прежде чем писать "не было X" или "официант не
сделал Y", ещё раз просмотри расшифровку целиком на предмет реплик именно
на эту тему — не полагайся на первое впечатление от беглого чтения.

**4. Оценивай качество исполнения, а не только сам факт разговора на тему.**
Если официант заговорил о нужной теме (концепция заведения, состав блюда),
это ещё не значит, что тема раскрыта хорошо. Проверь дословную цитату: если
рассказ путаный, неуверенный, самоопровергающий ("у нас нет какого-то
определённого перевода...") — это низкий балл с комментарием о низком
качестве объяснения, а не автоматический максимум за сам факт разговора.

**5. Тайминги — перепроверяй цифры, а не оценку "быстро/медленно" на глаз.**
Если критерий завязан на конкретное число минут (например, "напиток за
семь минут" против "напиток больше семи минут"), найди в расшифровке
максимально точный тайм-код события и посчитай разницу в минутах явно,
а не полагайся на субъективное ощущение "долго ждали" или "быстро принесли".
Если тайм-кода начала и конца события нет одновременно — это пробел для
фазы В, а не повод для приблизительной оценки.

**6. Указывай точное число, если оно есть в расшифровке, вместо общей оценки
"сразу"/"быстро"/"без задержки".** Пример: если тайм-код звонка [00:00:00] и
ответа [00:00:14] позволяют посчитать точное время ожидания, комментарий
должен содержать именно "через четырнадцать секунд", а не расплывчатое
"сразу, без ожидания". Точное число всегда сильнее и убедительнее общей
формулировки — считай разницу между тайм-кодами вручную, если это возможно.
"""


class InspectorFillError(Exception):
    pass


def _call_claude_via_openrouter(
    system_prompt: str, user_message: str, api_key: str, max_tokens: int = 32000
) -> str:
    """Вызывает Claude через OpenRouter — формат OpenAI chat completions,
    не нативный Anthropic Messages API (у OpenRouter system идёт как
    отдельное сообщение в messages, а не отдельным полем)."""
    payload = json.dumps(
        {
            "model": OPENROUTER_MODEL,
            "max_tokens": max_tokens,
            "reasoning": {"enabled": False},
            "messages": [
                {"role": "system", "content": system_prompt},
                {"role": "user", "content": user_message},
            ],
        }
    ).encode("utf-8")

    req = urllib.request.Request(
        OPENROUTER_API_URL,
        data=payload,
        headers={
            "Content-Type": "application/json",
            "Authorization": f"Bearer {api_key}",
        },
    )
    try:
        with urllib.request.urlopen(req, timeout=180) as response:
            result = json.loads(response.read().decode("utf-8"))
        choices = result.get("choices", [])
        if not choices:
            raise InspectorFillError(f"Пустой ответ от OpenRouter (нет choices): {result}")

        message = choices[0].get("message", {})
        content = message.get("content")
        finish_reason = choices[0].get("finish_reason")

        if not content:
            # content пустой или None — часто означает, что ответ обрезан по лимиту
            # токенов (finish_reason == "length"), либо модель ушла в reasoning
            # и не успела написать финальный текст.
            raise InspectorFillError(
                f"OpenRouter вернул пустой content (finish_reason={finish_reason}). "
                f"Вероятно, не хватило max_tokens для такого большого ответа. "
                f"Полный ответ: {json.dumps(result, ensure_ascii=False)[:500]}"
            )

        return content
    except urllib.error.HTTPError as e:
        body = e.read().decode("utf-8", errors="replace")
        raise InspectorFillError(f"Ошибка OpenRouter ({e.code}): {body[:300]}")


def _extract_json(text: str) -> dict:
    """Достаёт JSON-объект из ответа модели, даже если она обернула его в markdown-код."""
    cleaned = text.strip()
    if cleaned.startswith("```"):
        cleaned = cleaned.split("```")[1]
        if cleaned.startswith("json"):
            cleaned = cleaned[4:]
    cleaned = cleaned.strip()
    try:
        return json.loads(cleaned)
    except json.JSONDecodeError as e:
        raise InspectorFillError(f"Не удалось разобрать JSON от модели: {e}\nОтвет модели: {text[:500]}")


def analyze_transcript_and_fill(
    empty_template_path: str,
    transcript_text: str,
    intro_context: str,
    openrouter_api_key: str,
) -> dict:
    """Первый проход: разбор расшифровки, заполнение уверенных пунктов,
    формирование списка уточняющих вопросов."""
    structure = extract_template_structure(empty_template_path)
    template_text = build_compact_template_text(structure)

    user_message = f"""Вот структура пустой анкеты (структурный источник правды):

{template_text}

Вводная информация о визите: {intro_context}

Расшифровка визита:

{transcript_text}

Заполни анкету по правилам из системного промпта, верни результат строго в формате JSON, описанном в дополнении к формату ответа."""

    full_system_prompt = INSPECTOR_SYSTEM_PROMPT + OUTPUT_FORMAT_INSTRUCTIONS

    raw_response = _call_claude_via_openrouter(full_system_prompt, user_message, openrouter_api_key)
    parsed = _extract_json(raw_response)
    parsed["_structure"] = structure
    return parsed


def resolve_questions_answers(
    fill_result: dict,
    user_answers: dict[int, str],
) -> list[dict]:
    """Превращает ответы пользователя (номер вопроса -> выбранный label) в
    финальный список {"row": ..., "score": ..., "comment": ...} для дозаписи в xlsx."""
    resolved = []
    questions = fill_result.get("questions", [])

    for index, question in enumerate(questions, start=1):
        answer_label = user_answers.get(index)
        if answer_label is None:
            continue
        for option in question.get("options", []):
            if option.get("label") == answer_label:
                resolved.append(
                    {"row": option["row"], "score": option["score"], "comment": option["comment"]}
                )
                break

    return resolved
